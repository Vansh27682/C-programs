//Problem 1(a): Write a program to calculate area of a rectangle using hard coded inputs.
#include <stdio.h>

int main()
{
    int l = 12;
    int b = 15;
    printf("Length = %d", l);
    printf("\n Width = %d", b);
    int area;
    area= l*b;
    printf("\nThe area of the given rectangle is %d", area);
    return 0;
}
